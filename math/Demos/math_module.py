import math

print("\nmath.ceil()")
print(math.ceil(5.4)) # 6
print(math.ceil(-5.4)) # -5

print("\nmath.floor()")
print(math.floor(5.6)) # 5
print(math.floor(-5.6)) # -6

print("\nmath.trunc()")
print(math.trunc(5.6)) # 5
print(math.trunc(-5.6)) # -5

print("\nmath.fabs()")
print(math.fabs(-5)) # 5.0
print(abs(-5)) # 5

print("\nmath.factorial()")
print(math.factorial(3)) # 6
print(math.factorial(5)) # 120

print("\nmath.pow()")
print(math.pow(5, 2)) # 25.0

print("\nmath.sqrt()")
print(math.sqrt(25)) # 5.0