# Create veggies list
veggies = ['tomato', 'spinach', 'pepper', 'pea']
print(veggies)

# Remove first occurence of 'tomato'
veggies.remove('tomato')
print(veggies)

# Append 'corn'
veggies.append('corn')
print(veggies)

# Create meats list
meats = ['chicken', 'beef', 'pork', 'fish']
print(meats)

# Concatenate meats and veggies to create new foods list
foods = meats + veggies
print(foods)

# Sort foods in place
foods.sort()
print(foods)